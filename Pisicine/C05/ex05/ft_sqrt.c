int	ft_sqrt(int nb)
{
	int		a;
	int		b;

	a = -1;
	b = 0;
	if (nb > 0)
	{
		while (a < 46341)
		{
			a++;
			b = a * a;
			if (b == nb)
				return (a);
		}
		return (0);
	}
	return (0);
}
