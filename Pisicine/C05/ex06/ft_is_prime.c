int	ft_is_prime(int nb)
{
	int		a;
	int		b;

	b = 0;
	a = 0;
	if (nb <= 1)
		return (0);
	while (a != nb)
	{
		a++;
		if (0 == nb % a)
			b++;
	}
	if (b == 2)
		return (1);
	return (0);
}
